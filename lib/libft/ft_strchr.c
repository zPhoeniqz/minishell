#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	while (*s != ch && *s)
		s++;
	if (*s != ch)
		return (NULL);
	return ((char *)s);
}
